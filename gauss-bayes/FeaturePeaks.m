function [awpk] = table_creation(file,mode)
%takes in excel file and creates table with mode column
aw_data=readmatrix(file);
awa_x = smooth_filter(aw_data(:,2));
awa_y = smooth_filter(aw_data(:,3));
awa_z = smooth_filter(aw_data(:,4));
awa_abs = smooth_filter(aw_data(:,5));
awtime = aw_data(:,1);


%acceleration of x
[awpktx,awpkx]=minamp20peaks(1.87,awtime,awa_x);
awpkx1=awpkx(:);

%acceleration of y

[awpkty,awpky]=minamp20peaks(1.87,awtime,awa_y);
awpky1=awpky(:);

%acceleration of z
[awpktz,awpkz]=minamp20peaks(1.87,awtime,awa_z);
awpkz1=awpkz(:);

%absolute acceleration
[awpkta,awpka]=minamp20peaks(1.87,awtime,awa_abs);
awpka1=awpka(:);

classaw = repmat(mode,[1 length(awpka1)]);
classaw = classaw(:);
awpk=table(awpkx1,awpky1,awpkz1,awpka1,classaw);
awpk.Properties.VariableNames = ["A_xpeak","A_ypeak","A_zpeak","A_abspeak","Motion"]


end