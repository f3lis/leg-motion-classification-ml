function [x_p y_p] = minamp20peaks(v,x, y)
   
    x_p = [] ;
    y_p = [] ;
    a = 1 ; 
    
    for i = 1:length(x)
        if  i == 1 || i == length(x)
            a;
            continue
            
        elseif (y(i+1) < y(i)) && (y(i-1) < y(i)) && y(i)>v
            
                x_p(a) = x(i) ;
                y_p(a) = y(i) ;
                a = a + 1 ;
                
        elseif a>20
               a;
               break
        else 
            continue
                
        end
    
    end
end    

